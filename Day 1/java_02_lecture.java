public class java_02_lecture {
    // Declare and initialize variables
    public static void main(String[] args) {
        int age = 25;
        double height = 1.75;
        String name = "John Doe";
        boolean isStudent = true;

        // Print variable values
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + " meters");
        System.out.println("Is student: " + isStudent);

        // Perform some operations using variables
        int futureAge = age + 5;
        double doubledHeight = height * 2;

        // Print the results of the operations
        System.out.println("After 5 years, age will be: " + futureAge);
        System.out.println("Double the height: " + doubledHeight);
    }
}
